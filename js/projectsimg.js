// IMG1

var holder = document.getElementById("projects-holder");
var img = document.getElementById("projects-img");
var result = document.getElementById("procontent");

holder.addEventListener("mouseover", function () {
      img.style.transition = " .6s ease";
      img.style.opacity = .6;
      result.style.transition = " 1s ease";
      result.style.display = "block";
      result.style.position = "absolute";
});

result.addEventListener("mouseover", function () {
      result.style.transition = " .6s ease";
      result.style.display = "block";
      result.style.position = "absolute";
      img.style.transition = " .6s ease";
      img.style.opacity = .6;
      result.style.position = "absolute";
});

holder.addEventListener("mouseout", function () {
      img.style.transition = " .6s ease";
      img.style.opacity = 1;
      result.style.transition = " 1s ease";
      result.style.display = "none";
});

result.addEventListener("mouseout", function () {
      result.style.transition = " .6s ease";
      result.style.display = "none";
      img.style.transition = " .6s ease";
      img.style.opacity = 1;
});


 

